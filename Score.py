scores = input().split()
fail = 0
max = 0
min = 100

for i in range(0, len(scores)):
  if (int(scores[i]) < 60):
    fail += 1
  if (int(scores[i]) > max):
    max = int(scores[i])
  if (int(scores[i]) < min):
    min = int(scores[i])

print("不及格的數量為：{}".format(fail))
print("max:{} min:{}".format(max,min))